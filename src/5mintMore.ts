import {
  Connection,
  clusterApiUrl,
  Keypair,
  PublicKey,
} from "@solana/web3.js";
import {
  getMint,
  getAccount,
  mintTo,
} from "@solana/spl-token";
import * as fs from "node:fs";
import * as path from "node:path";

async function main() {
  /* ‚îÄ‚îÄ 1. Load wallet.json ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const secret = Uint8Array.from(
    JSON.parse(fs.readFileSync(path.resolve("wallet.json"), "utf8"))
  );
  const wallet = Keypair.fromSecretKey(secret);

  /* ‚îÄ‚îÄ 2. Load token.json ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const { tokenAddress, tokenAccount } = JSON.parse(
    fs.readFileSync(path.resolve("token.json"), "utf8")
  ) as { tokenAddress: string; tokenAccount: string };

  const MINT = new PublicKey(tokenAddress);
  const ATA  = new PublicKey(tokenAccount);

  /* ‚îÄ‚îÄ 3. Connect to devnet ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const connection = new Connection(clusterApiUrl("devnet"), "confirmed");

  /* ‚îÄ‚îÄ 4. Fetch mint + decimals ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const mintInfo = await getMint(connection, MINT);
  const decimals = mintInfo.decimals;

  /* ‚îÄ‚îÄ 5. Amount = 100 whole tokens ‚Üí base units ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const wholeTokens = 100n;
  const amount      = wholeTokens * 10n ** BigInt(decimals);

  /* ‚îÄ‚îÄ 6. Mint! ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const sig = await mintTo(
    connection,
    wallet,          // payer + authority
    MINT,
    ATA,
    wallet,          // mint authority
    amount
  );

  /* ‚îÄ‚îÄ 7. Show new balance ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
  const acctInfo = await getAccount(connection, ATA);
  const uiBalance =
    Number(acctInfo.amount) / 10 ** decimals;

  console.log("‚úÖ  Minted", wholeTokens.toString(), "tokens");
  console.log("üñãÔ∏è  Signature:", sig);
  console.log("üí∞  New balance:", uiBalance.toLocaleString(), "tokens");
}

main().catch((e) => {
  console.error("‚ùå  Error:", e);
  process.exit(1);
});
